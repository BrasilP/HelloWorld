

# Input function



# Notice that in both cases, the result outcome is a string value

name = input("Please enter your name.")
print("Your name is " + name + ".")
print(type(name))

fav_num = input("What is your favorite number?")
print("Your favorite number is " + fav_num + ".")
print(type(fav_num))





# Int & Float functions

# If we want the function to give back an integer or a float rather than a string we could use the following functions
user_int = int(input("Please enter an integer."))
print(user_int)
print(type(user_int))

user_float = float(input("Please enter a float."))
print(user_float)
print(type(user_float))
